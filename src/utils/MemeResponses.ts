    static getScoreEmoji(score: number): string {
        if (score >= 2000) return "👑";
        if (score >= 1000) return "⭐";
        if (score >= 500) return "✅";
        if (score >= 0) return "😐";
        if (score >= -200) return "⚠️";
        if (score >= -500) return "❌";
        return "💀";
    }

